using System;
using System.Collections.Generic;
using System.Text;

namespace WD_toolbox.Data.Extensions
{
    public static class DictionaryExt
    {
        public static void AddIfNotExist<K, V>(this Dictionary<K, V> dict, K key, V value)
        {
            if (!dict.ContainsKey(key))
            {
                dict.Add(key, value);
            }
        }
    }
}
